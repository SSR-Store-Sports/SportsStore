    <h1>Guia de Medidas — Roupas Esportivas Femininas</h1>
    <br>
    <p>Medir corretamente facilita na hora de escolher o tamanho ideal e evita trocas. Use uma fita métrica flexível, vista roupa íntima fina e peça ajuda quando possível.</p>
<br>
    <h2>Como medir</h2>
    <br>

    <h3>1. Busto</h3>
    <br>
    <p>Meça ao redor da parte mais cheia do busto, com a fita paralela ao chão. Não aperte demais.</p>
    <br>

    <h3>2. Abaixo do busto (tórax)</h3>
    <br>
    <p>Meça logo abaixo do busto — importante para escolher tops esportivos com boa sustentação.</p>
    <br>
    <br>

    <h3>3. Cintura</h3>
    <br>
    <p>Meça a parte mais estreita do tronco (acima do umbigo). Mantenha a fita reta e confortável.</p>
    <br>
    <br>

    <h3>4. Quadril</h3>
    <br>
    <p>Meça na parte mais larga do quadril e bumbum, com a fita paralela ao chão.</p>
    <br>


    <h3>5. Entrepernas (inseam)</h3>
    <br>
    <p>Meça do ponto entre as pernas até o tornozelo ou use uma peça que sirva como referência.</p>
    <br>

    <h2>Dicas rápidas</h2>

    <br>
    <ul>
      <li>Meça duas vezes para confirmar.</li>
      <li>Para leggings de compressão, se estiver entre dois tamanhos, prefira o menor.</li>
      <li>Para tops, se quiser mais conforto, escolha o maior.</li>
      <li>Anote suas medidas em centímetros.</li>
    </ul>
    <br>

   

