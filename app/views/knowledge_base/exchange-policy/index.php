    <H1 class="title-policy">
        Política de Trocas e Devoluções - TatifitWear
    </H1>
    <br>
    <p>

Na TatifitWear, queremos que sua experiência de compra seja sempre a melhor possível. Sabemos que, ao comprar roupas esportivas online, surgem dúvidas sobre tamanho, caimento e outras características. Por isso, criamos uma política de trocas e devoluções clara, simples e transparente, garantindo segurança e tranquilidade para você. 

  </p> 
  <br> 
    <h2>1. Prazo para Trocas e Devoluções</h2>
    <p>
    <br>

Você tem até 30 dias corridos, a partir da data de recebimento do produto, para solicitar trocas ou devoluções. Após esse período, não poderemos aceitar solicitações.
    </p>
    <br>

    <h2>2. Condições para Trocas e Devoluções</h2>
    <br>
    <p>
Para que possamos processar sua troca ou devolução, o produto deve estar nas seguintes condições:
    </p>

    <br>
    <ul>
        <li> Com a etiqueta original afixada;</li>
        <li> Sem sinais de uso, lavagem ou danos;</li>
        <li> Em sua embalagem original;</li>
        <li> Com a nota fiscal ou comprovante de compra.</li>
    </ul>
    <br>
    <h2>3. Processo de Troca e Devolução</h2>
    <br>
    <p>
Para iniciar o processo de troca ou devolução, siga os passos abaixo:
    </p>
    <br>
    <ul>
        <li> Entre em contato com nosso atendimento ao cliente pelo e-mail suporte@tatifitwear.com.br;</li>
        <li> Informe o número do pedido e o motivo da troca ou devolução;</li>
        <li> Aguarde as instruções para o envio do produto;</li>
        <li> Envie o produto conforme as orientações recebidas;</li>
        <li> Após o recebimento e análise do produto, efetuaremos a troca ou reembolso.</li>
    </ul>
    <br>
    <h2>4. Reembolso</h2>
    <p>
    <br>
Os reembolsos serão processados utilizando a mesma forma de pagamento escolhida no momento da compra. O prazo para o crédito do valor pode variar conforme a instituição financeira.
    </p>
    <br>
    <h2>5. Itens que Não Podem Ser Trocados ou Devolvidos</h2>
    <br>
    <p>
Por questões de higiene e segurança, não aceitamos trocas ou devoluções de produtos como roupas íntimas, acessórios e itens em promoção.
    </p>
    <br>
    <h2>6. Contato</h2>
    <br>
    <p>
Se você tiver qualquer dúvida ou precisar de assistência, nossa equipe de atendimento ao cliente está à disposição para ajudar. Entre em contato conosco pelo e-mail suporte@tatifitwear.com.br.
    </p>
    <p>
Agradecemos por escolher a TatifitWear. Estamos comprometidos em oferecer a melhor experiência de compra para você! 
    </p>
